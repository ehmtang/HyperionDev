# Dictionary containing country (key) and its capital city (value)
countryMap = {
    'United Kingdom' : 'London',
    'France' : 'Paris',
    'Spain' : 'Madrid',
    'Italy' : 'Rome',
    'Germany' : 'Berlin',
    'Sweden' : 'Stockholm'
}

# print dict[key] returns value of input key
print(countryMap['Sweden']) # ='Stockholm'
print(countryMap['Spain']) # ='Madrid'
print(countryMap['Germany']) # ='Berlin'
